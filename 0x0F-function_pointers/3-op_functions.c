#include "3-calc.h"

/**
 * op_add - that add a and b
 * @a: an integer
 * @b: an integer
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - that substact b from a
 * @a: an integer
 * @b: an integer
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - that multiple a and b
 * @a: an integer
 * @b: an integer
 * Return: a*b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - that divise a on b
 * @a: an integer
 * @b: an integer
 * Return: a/b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - that get the reminder of division of a by b
 * @a: an integer
 * @b: an integer
 * Return: a%b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
